                        <strong>2. What is automatic route?</strong>
                        <br>
                        <p>Ans: Foreign investments made under an automatic route do not require prior approval from
                            regulatory bodies established by the Indian government. A post facto intimation to the RBI
                            within 30 days of shares being allocated to overseas investors is the only requirement.</p>

                        <strong>3. Does a director/shareholder have to be physically present for incorporating an Indian
                            Subsidiary?</strong>
                        <br>
                        <p>Ans: No, it is not mandatory for a director/shareholder to be physically present for
                            incorporating an Indian Subsidiary.</p>

                        <strong>4. Can NRIs/Foreign nationals hold shares in an Indian Subsidiary?</strong>
                        <br>
                        <p>Ans: Yes, NRIs/Foreign Nationals can hold shares in an Indian Subsidiary, provided they adhere to
                            the FDI guidelines issued by the government before issuing shares..</p>

                        <strong>5. Is it possible for a foreign company to use e-MoA and e-AoA for incorporating an Indian
                            Subsidiary?</strong>
                        <br>
                        <p>Ans: No, the MoA and AoA signed by an overseas national must be duly notarized/apostilled in the
                            country of origin, as required by Rule 13(5) of the Company (Incorporation) Rules under the
                            Companies Act, 2013.</p>

                        <strong>6. What is the qualification for an individual to be a director of an Indian
                            Subsidiary?</strong>
                        <br>
                        <p>Ans: There is no mandatory qualification for an individual to be a director of an Indian
                            Subsidiary, and any natural person above the age of 18 can become a director.</p>

                        <strong>7. What is a DIN?</strong>
                        <br>
                        <p>Ans: A DIN, or Director Identification Number, is a unique identifier for a company director.
                            When a person is appointed as a company director, the DIN must be mentioned in the relevant
                            documentation.</p>

                        <strong>8. What is DSC?</strong>
                        <br>
                        <p>Ans: A Digital Signature Certificate (DSC) is an electronic signature of a person in encrypted
                            form. DSC is used for signing e-forms (electronic forms) while filing documents for company
                            registration.</p>

